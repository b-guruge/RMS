function check_spaces(val) {
    //check for any kind of white space in a string and returns true if a space is found
    var pattern = /\s/;
    return pattern.test(val);
}

function pad(str, max) {
    str = str.toString();
    return str.length < max ? pad("0" + str, max) : str;
}

function getCurrentDate()
{
    var today = new Date();
    var dd = today.getDate();

    var mm = today.getMonth()+1; 
    var yyyy = today.getFullYear();
    if(dd<10) 
    {
        dd='0'+dd;
    } 

    if(mm<10) 
    {
        mm='0'+mm;
    } 

    return yyyy+'-'+mm+'-'+dd;
}